!<Calculates array of Imn special functions.

!------------------------------------------------------------------------------

subroutine calc_Imn_array(x1_in, x2_in, Imn)

implicit none;

integer, parameter :: dp = 8;
integer, parameter :: dpc = 8;
complex(8), parameter :: I = cmplx(0.0d0, 1.0d0, 8), one = cmplx(1.0d0, 0.0d0, 8);

complex(dpc) :: x1_in, x2_in;
complex(dpc), dimension(0:3,0:3) :: Imn;

complex(dpc) :: t1, t2, F11m;
real(dp) :: F_im, F_re;

complex(dpc), dimension(1:6) :: x1;
complex(dpc), dimension(1:2) :: x2;

x1(1) = x1_in;
x1(2) = x1(1)*x1(1);
x1(3) = x1(1)*x1(2);
x1(4) = x1(1)*x1(3);
x1(5) = x1(1)*x1(4);
x1(6) = x1(1)*x1(5);

x2(1) = x2_in;
x2(2) = x2(1)*x2(1);

t1 = x1(2);
t2 = - I*x2(1) + t1;

call hypergeometric1f1_cont_fract_1_modified_0_ada (real(one+t2), aimag(one+t2), real(t1), aimag(t1), F_re, F_im);

F11m = F_re + I*F_im;

!asymptotic form of expressions: has no fake singularity at kp=0

Imn(0,0) =  &
((3 + F11m)*x1(4) + x1(2)*(5 - (3*I)*x2(1)) - (I + x2(1))*(2*I + x2(1)))/ &
 ((x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(0,1) =  &
((-I)*x1(1)*(2 + 3*x1(2) + x1(4)) + x1(1)*(-1 + F11m*x1(2))*x2(1))/ &
 ((x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(0,2) =  &
((1 - I*x2(1))*(2 + x1(4) - I*x2(1) + x1(2)*(3 + I*F11m*x2(1))))/ &
 ((x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(0,3) =  &
(x1(1)*((-I)*x1(4) + (2*I + x2(1))*(-3 + F11m*x2(1)*(I + x2(1))) -  &
   I*x1(2)*(7 + 2*F11m + x2(2))))/((x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))* &
  (2 + x1(2) - I*x2(1)))

Imn(1,1) =  &
(x2(1)*((-I)*(2 + 3*x1(2) + x1(4)) + (-1 + F11m*x1(2))*x2(1)))/ &
 ((x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(1,2) =  &
(x1(1)*(-2*I - I*x1(4) + x2(1)*(-3 + F11m*x2(1)*(I + x2(1))) -  &
   I*x1(2)*(3 + x2(1)*(-2*I + x2(1)))))/((x1(2) - I*x2(1))* &
  (1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(1,3) =  &
((-I)*x2(1)*(x1(4) + I*(2*I + x2(1))*(-3 + F11m*x2(1)*(I + x2(1))) +  &
   x1(2)*(7 + 2*F11m + x2(2))))/((x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))* &
  (2 + x1(2) - I*x2(1)))

Imn(2,2) =  &
((1 - I*x2(1))*(2 + x1(4) + I*x2(1)*(-3 + F11m*x2(1)*(I + x2(1))) +  &
   x1(2)*(3 + x2(1)*(-2*I + x2(1)))))/((x1(2) - I*x2(1))* &
  (1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(2,3) =  &
((-3*I)*x1(6) - I*x1(4)*(9 + x2(1)*(-8*I + x2(1))) +  &
  F11m*(I + x2(1))**2*(2*I + x2(1))*x2(2) +  &
  x1(2)*(-6*I + x2(1)*(-11 + (2*I)*x2(1) + (1 - I*x2(1))*(2*F11m + x2(2)))))/ &
 (x1(1)*(x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

Imn(3,3) =  &
(x1(6)*(4 - (3*I)*x2(1)) -  &
  x1(4)*(-4 + 4*F11m + x2(1)*(25*I + (8 + I*x2(1))*x2(1))) +  &
  F11m*x2(2)*(-2 + (3*I)*x2(1) + x2(2))**2 + x1(2)*(2 - I*x2(1))*x2(1)* &
   (-11*I - 2*x2(1) + (I + x2(1))*(4*F11m + x2(2))))/ &
 (x1(1)**2*(x1(2) - I*x2(1))*(1 + x1(2) - I*x2(1))*(2 + x1(2) - I*x2(1)))

!symmetry:
Imn(1,0) = Imn(0,1);
Imn(2,0) = Imn(0,2);
Imn(2,1) = Imn(1,2);
Imn(3,0) = Imn(0,3);
Imn(3,1) = Imn(1,3);
Imn(3,2) = Imn(2,3);

!do m = 0,3
!    do n = 0,m-1
!        Imn(m,n) = Imn(n,m)
!    end do
!end do

end subroutine

