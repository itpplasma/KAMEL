!<Calculates array of Imn special functions - the same as _new.

!------------------------------------------------------------------------------

subroutine calc_Imn_array(x1_in, x2_in, Imn)

implicit none;

integer, parameter :: dp = 8;
integer, parameter :: dpc = 8;
complex(8), parameter :: I = cmplx(0.0d0, 1.0d0, 8), one = cmplx(1.0d0, 0.0d0, 8);

complex(dpc) :: x1_in, x2_in;
complex(dpc), dimension(0:3,0:3) :: Imn;

complex(dpc) :: t1, t2, F11m;
real(dp) :: F_im, F_re;

complex(dpc), dimension(1:6) :: x1;
complex(dpc), dimension(1:2) :: x2;

x1(1) = x1_in;
x1(2) = x1(1) *x1(1);
x1(3) = x1(1) *x1(2);
x1(4) = x1(1) *x1(3);
x1(5) = x1(1) *x1(4);
x1(6) = x1(1) *x1(5);

x2(1) = x2_in;
x2(2) = x2(1) *x2(1);

t1 = x1(2);
t2 = - I*x2(1) + t1;

call hypergeometric1f1_cont_fract_1_modified_0_ada (real(one+t2), aimag(one+t2), real(t1), aimag(t1), F_re, F_im);

F11m = F_re + I*F_im;

!asymptotic form of expressions: has no fake singularity at kp=0

Imn(0,0) = (-(I + x2(1))*(2.0*I + x2(1)) + (5.0 - 3.0*I*x2(1))*x1(2) + (3.0 + F11m)*x1(4)) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(0,1) = (x1(1)*x2(1)*(-1.0 + F11m*x1(2)) - I*x1(1)*(2.0 + 3.0*x1(2) + x1(4))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(0,2) = ((1.0 - I*x2(1))*(2.0 - I*x2(1) + (3.0 + I*F11m*x2(1))*x1(2) + x1(4))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(0,3) = (x1(1)*((2.0*I + x2(1))*(-3.0 + F11m*x2(1)*(I + x2(1))) - I*(7.0 + 2.0*F11m + x2(2))*x1(2) - I*x1(4))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(1,1) = (x2(1)*(x2(1)*(-1.0 + F11m*x1(2)) - I*(2.0 + 3.0*x1(2) + x1(4)))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(1,2) = (x1(1)*(-2.0*I + x2(1)*(-3.0 + F11m*x2(1)*(I + x2(1))) - I*(3.0 + x2(1)*(-2.0*I + x2(1)))*x1(2) - I*x1(4))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(1,3) = -((I*x2(1)*(I*(2.0*I + x2(1))*(-3.0 + F11m*x2(1)*(I + x2(1))) + (7.0 + 2.0*F11m + x2(2))*x1(2) + x1(4))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2))));

Imn(2,2) = ((1.0 - I*x2(1))*(2.0 + I*x2(1)*(-3.0 + F11m*x2(1)*(I + x2(1))) + (3.0 + x2(1)*(-2.0*I + x2(1)))*x1(2) + x1(4))) / &
           ((-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(2,3) = (-I*(9.0 + x2(1)*(-8.0*I + x2(1)))*x1(4) - 3.0*I*x1(6) + F11m*(I + x2(1))*(I + x2(1))*(2.0*I + x2(1))*x2(2) + &
           x1(2)*(-6.0*I + x2(1)*(-11.0 + 2.0*I*x2(1) + (1.0 - I*x2(1))*(2.0*F11m + x2(2))))) / &
           (x1(1)*(-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

Imn(3,3) = (-(-4.0 + 4.0*F11m + x2(1)*(25.0*I + (8.0 + I*x2(1))*x2(1)))*x1(4) + (4.0 - 3.0*I*x2(1))*x1(6) + &
           F11m*x2(2)*(-2.0 + 3.0*I*x2(1) + x2(2))**2 + (2.0 - I*x2(1))*x2(1)*x1(2)*(-11.0*I - 2.0*x2(1) + &
           (I + x2(1))*(4.0*F11m + x2(2)))) / (x1(2)*(-I*x2(1) + x1(2))*(1.0 - I*x2(1) + x1(2))*(2.0 - I*x2(1) + x1(2)));

!symmetry:
Imn(1,0) = Imn(0,1);
Imn(2,0) = Imn(0,2);
Imn(2,1) = Imn(1,2);
Imn(3,0) = Imn(0,3);
Imn(3,1) = Imn(1,3);
Imn(3,2) = Imn(2,3);

!do m = 0,3
!    do n = 0,m-1
!        Imn(m,n) = Imn(n,m)
!    end do
!end do

end subroutine

