      SUBROUTINE RF(Z,FZ)
C
C    THIS SUBROUTINE IS USED BY 'EXROOT' IN THE REFINEMENT
C  OF ROOTS BY 'MULLER'.  REMEMBER THAT IF THE REFINEMENT
C  PROCESS WITHIN 'EXROOT' IS REPLACED BY A ROUTINE SUCH
C  AS THE NEWTON-RAPHSON PROCESS THAT EMPLOYS THE DERIVATIVE
C  THEN THIS NEED TO BE RESCALED AS IS INDICATED BELOW.
C
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE COMPLEX Z,FZ,DZ,ZP,CEN
C
      COMMON /RSCALE/ SCALE,CEN
C
C
      ZP=CEN+SCALE*Z
      CALL FNDER(ZP,FZ,DZ)
C     DZ=SCALE*DZ
C     RESCALING OF DERIVATIVE.
C
      RETURN
      END
